package com.carollim.myrecycleapp;

public class ChartPie {
    String kg;
    String label;

    public ChartPie(String kg, String label) {
        this.kg = kg;
        this.label = label;
    }

    public String getKg() {
        return kg;
    }

    public void setKg(String kg) {
        this.kg = kg;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
